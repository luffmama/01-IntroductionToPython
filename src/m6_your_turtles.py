import rosegraphics as rg
phil =  rg.SimpleTurtle('turtle')
phil.pen = rg.Pen('green', 24)
phil.speed = 15
for k in range(50):
    phil.left(15)
    phil.forward(k)
jessica = rg.SimpleTurtle('circle')
jessica.speed = 50
jessica.pen = rg.Pen('red', 2)
jessica.backward(75)
for k in range(250):
    jessica.forward(k+75)
    jessica.left(190)

